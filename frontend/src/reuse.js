import { toast } from "react-toastify";



export const handleLogout = (navigate) => {
    localStorage.removeItem("auth");
    navigate("/")
  };
export const showSuccessDialog = (message) => {
  toast.success(message, {
    position: "top-right",
    autoClose: 5000,
    hideProgressBar: false,
    closeOnClick: true,
    pauseOnHover: true,
    draggable: true,
    progress: undefined,
    theme: "light",

  });
};
